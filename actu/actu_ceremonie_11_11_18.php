    <h2>Cérémonie de l’Armistice du 11 novembre</h2>
    <p><strong>ARMISTICE 11 NOVEMBRE - Après la commémoration à l'Arc de Triomphe par 72 chefs d'Etat internationaux dont Macron,
      Trump et Merkel, le centenaire continue d'être célébré à travers de nombreuses expositions à Paris.</strong></p>
      <img src="../images/ceremonie.jpg" alt="Macron pendant la cérémonie du 11-11-18" class="image_article" title="Macron pendant la cérémonie du 11 novembre 2018"/>
  <p>Dimanche 11 novembre 2018, l'Élysée a célébré en grande pompe le centenaire de l'armistice de 1918 en présence de nombreux
    chefs d'État invités par Emmanuel Macron, dont le président américain Donald Trump, qui a vu son convoi bloqué par plusieurs
    militantes Femen avant d'arriver à l'Arc de Triomphe. Après une cérémonie de commémoration du centenaire de l'Armistice
    pluvieuse sur la place de l'Étoile autour de l'Arc de Triomphe, marquée par l'arrivée tardive de Donald Trump et de Vladimir
    Poutine, les chefs d'État se sont rendus à La Villette pour un Forum sur la paix où s'exprimaient notamment Emmanuel Macron
    et le Premier ministre du Canada Justin Trudeau. Donald Trump, chantre de l'"America First", n'a pas souhaité assister à ce
    forum appelé à devenir annuel, et a rendu hommage aux soldats américains tombés pendant la guerre au cimetière de Suresnes.
    Une manifestation a été organisée dans l'après-midi du 11 novembre pour protester contre la venue du président américain à
    Paris. Le président français a tenu ce dimanche un discours émouvant pour le centenaire de l'Armistice de 1918, qui insistait
    sur le fait de tirer des leçons du passé : "La France sait ce qu'elle doit à ses combattants et à tous les combattants venus
    du monde entier. Elle s'incline devant leur grandeur. La France salue avec respect et gravité les morts des autres nations
    que, jadis, elle a combattues. Elle se tient à côté d'elles. ""Nos pieds ne se détachent qu'en vain du sol qui contient les
    morts"" écrivait Guillaume Apollinaire. Que sur les tombes où ils reposent, fleurisse la certitude qu'un monde meilleur est
     possible si nous le voulons, si nous le décidons, si nous le construisons, si nous l'exigeons de toute notre âme. En ce 11
     novembre 2018, cent ans après un massacre dont la cicatrice est encore visible sur la face du monde, je vous remercie pour
     ce rassemblement de la fraternité retrouvée du 11 novembre 1918. Puisse ce rassemblement ne pas être seulement celui d'un
     jour. Cette fraternité, mes amis, nous invite, en effet, à mener ensemble le seul combat qui vaille : le combat de la paix,
      le combat d'un monde meilleur." Après le 11 novembre 2018, le centenaire continue d'être célébré avec des événements et expositions organisés en France et dans la capitale jusqu'en janvier 2019.
