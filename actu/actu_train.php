      <h2>Le train d'or des nazis retrouvé en Pologne serait piégé</h2>
      <img src="../images/train.jpg" alt="Le train aurait été caché dans un des nombreux tunnels creusés par les nazis./AFP" class="image_article" title="Le train aurait été caché dans un des nombreux tunnels creusés par les nazis./AFP"/>
      <p>Deux chasseurs de trésor de nationalité allemande et polonaise auraient localisé un train chargé d'or, caché par
        les nazis en avril 1945. Il aurait été retrouvé à Walbrzych, une ville au Sud-Ouest de la Pologne près du château de Ksiaz.
         Ce dernier, situé près de la frontière tchèque, servait alors de quartier général aux nazis pendant la Seconde guerre
         mondiale.

Le train aurait été caché dans un des très nombreux tunnels secrets creusés par les nazis afin d'enfouir l'industrie de
l'armement pour la mettre à l'abri des bombardements alliés. Ce véritable dédale de dizaines de kilomètres de galeries a été
creusé par les prisonniers et déportés durant la Seconde guerre mondiale. Cet énorme chantier baptisé du nom de code de «Riese»
(Géant) est aujourd'hui devenu pour partie une attraction touristique qui se visite. Mais les deux découvreurs auraient trouvé
une rame blindée de 120 à 150 mètres de long dans un des tunnels non encore explorés. La zone qu'ils ont fouillée n'aurait jamais
été explorée par le passé. Les chasseurs de trésor ont adressé aux autorités polonaises une demande officielle qui leur
permettra d'obtenir 10 % de la valeur du contenu du train caché par le troisième Reich. Le maire de Walbrzych a confirmé que
le train chargé d'or et de bijoux se trouvait bien sur sa commune. Les autorités polonaises ont demandé à tous les chasseurs de
trésor attirés par cette histoire de cesser toutes leurs recherches afin de les confier uniquement à la police, à l'armée et aux
pompiers. Une précaution car un vrai risque d'explosion existe. «Dans le train caché, dont je suis convaincu qu'il existe, peuvent
se trouver des matières dangereuses du temps de la Seconde guerre mondiale. Il est très probable que le train est miné», a prévenu
le conservateur général des monuments Piotr Zuchowski, secrétaire d'Etat au ministère de la Culture et du Patrimoine. De son côté,
le maire adjoint a été plus prudent se refusant à évoquer «un train» lui préférant le terme de «trouvaille». Quelle que soit la
dénomination donnée, les deux chasseurs de trésor ont bien trouvé quelque chose : «L'endroit est gardé secret», a précisé le
maire-adjoint. En vertu d'accords passés avec l'Allemagne, s'il y a bien un trésor, c'est l‘État polonais qui le conservera.
Seul problème et de taille : si une partie de cet or a été volée dans les coffres des banques centrales à travers l'Europe par
les nazis, l'autre provenance des métaux précieux et des bijoux reste les familles juives qui ont été spoilées. Sans compter que
le creusement de ces tunnels a été effectué par les prisonniers des camps de concentration. Source <a href="https://www.ladepeche.fr/article/2015/08/28/2166279-train-or-nazis-retrouve-pologne-serait-piege.html" title="Article de Ladepech">Ladepeche.</a></p>
