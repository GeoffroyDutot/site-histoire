    <h2>Le soldat inconnu à désormais un visage</h2>
          <img src="../images/soldat_inconnu.jpg" alt="Le visage du Soldat inconnu. (© Historial de la Grande Guerre)" class="image_article" title="Le visage du Soldat inconnu. (© Historial de la Grande Guerre)"/>
      <p>Grâce à un algorithme qui a combiné 30 000 visages figurant sur des photos, de soldats,
mais aussi d’hommes et de femmes ayant vécu la Grande Guerre, le Soldat inconnu a désormais un visage.
Ce portrait-robot a été réalisé par l’Historial de la Grande Guerre de Péronne,
à l’occasion du centenaire de l’armistice qui a été commémoré ce week-end.
La juxtaposition d’autant d’images d’archive (30 000 photos d’époque au total)
a été le fruit d’un long travail de recherche effectué sur plus de dix mois.
Il a fallu récolter des documents photographiques centenaires, dans différents pays.
Soldats français, canadiens, américains, italiens, russes ou encore des troupes coloniales,
infirmières sur le champ de bataille ou victimes tombées pour la France… <br><br>
Le visage de ce Soldat inconnu est maintenant pluriel et rend hommage à toutes ces personnes qui ont participé au combat.
<em>"Au lendemain de la Première Guerre mondiale, à Paris comme à Londres, l’inhumation d’un Soldat inconnu incarne la mémoire
de tous les soldats morts. L’anonymat garantit l’héroïsme de tous et permet aux familles de faire leur deuil.
[…] Un symbole universel de paix, d’espoir et de mémoire en hommage à toutes les vies brisées par la Grande Guerre,
quels que soient leurs nationalités, leurs origines et leur sexe"</em>, peut-on lire sur le site. Sur le site officiel,
on peut ainsi parcourir les 30 000 photos qui ont servi à l’algorithme, et également lire les histoires de quelques-uns.
Ce dispositif est aussi visible à l’Historial de la Grande Guerre. Jean-Luc Dron, collectionneur et auteur sur la Grande Guerre
qui a pris part au projet, explique : <em>"On est tellement habitués à voir des listes de noms sur les monuments aux morts,
que c’est important de pouvoir mettre un visage sur ces noms, et dans le cadre de ce projet, je pense que l’occasion est
exceptionnelle. Pour moi, c’était important de sortir ces livres de ma bibliothèque et de les rendre accessibles."</em> <br><br>
Le développeur Grégoire Divaret-Chauveau rajoute : <em>"Le but était de créer un portrait qui soit cohérent,
à partir de photos sur lesquelles les visages sont positionnés et orientés de façon très différente.
Pour arriver à ce but, sur chacun des portraits, on va appliquer un algorithme de détection du visage,
qui va nous indiquer où sont situés les contours du visage, le nez, les yeux, la bouche, et ensuite on
va transformer chacune des images pour les faire correspondre. Plus on ajoute de portraits au visage inconnu,
plus le centre du portrait va être net mais les contours vont faire apparaître des variations sur les oreilles
et les uniformes. Avec l’accumulation, cela va gommer toutes ces différences et on va avoir ce qui est commun à tous les hommes."</em>
Source <a href="https://www.konbini.com/fr/photographes/le-soldat-inconnu-a-desormais-un-visage-grace-a-30-000-photos" title="Article de Konbini">Konbini.</a></p>
