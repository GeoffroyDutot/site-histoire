<h2>Le débarquement de Normandie</h2>
<p>Le débarquement de Normandie, également appelé débarquement en Normandie, ou encore débarquement allié en Normandie, nom de code opération Neptunec, est une opération militaire amphibie alliée de la Seconde Guerre mondiale lancée dans la nuit du 5 au 6 juin 1944.

C'est la phase d'assaut d'une plus vaste opération, l'opération Overlord, qui vise à créer une tête de pont alliée de grande échelle dans le nord-ouest de l'Europe et l'ouverture d'un nouveau front à l'Ouest. Ce débarquement marque le début de la bataille de Normandie.

Cette opération Neptune incluait de nombreux mouvements :

la traversée de la Manche par plusieurs milliers de navires ;
les opérations aéroportées américaine et britannique la nuit précédente ;
les bombardements préparatoires aériens et navals des défenses côtières allemandes ;
le débarquement des troupes dès le 6 juin au matin (« Jour J ») sur les plages du nord-est du Cotentin et de l'ouest du Calvados dans les secteurs (d'ouest en est) d'Utah Beach et Omaha Beach et de la pointe du Hoc pour les Américains, Gold Beach pour les Britanniques, Juno Beach pour les Canadiens et Sword Beach pour les Britanniques (incluant les Français libres des commandos Kieffer).
Une fois les plages prises, l'opération se poursuit par la jonction des forces de débarquement et l'établissement d'une tête de pont sur la côte normande puis l'acheminement d'hommes et de matériels supplémentaires. Les jours suivants voient la mise en place des structures logistiques (ports artificiels Mulberry, oléoduc sous-marin PLUTO) pour le ravitaillement du front et le débarquement de troupes supplémentaires. L'opération cesse officiellement le 30 juin 1944.


Entre le 27 mai et le 4 juin 1940, plus de 338 000 hommes du Corps Expéditionnaire Britannique et de l'Armée Française, encerclées sur les côtes du nord de la France, regagnent le Royaume-Unis grâce à l'évacuation de Dunkerque. La signature de l'armistice puis l'occupation allemande en France prive les alliés de l'Europe de l'Ouest continentale. Après l'invasion de l'Union Soviétique par l'armée allemande en juin 1941, Joseph Staline commence à demander aux alliés l'ouverture d'un second front en Europe de l'Ouest. Fin mai 1942, les États-Unis et l'Union Soviétique font une déclaration commune sur l'urgence de créer un second front à l'ouest. Mais le premier ministre britannique Winston Churchill, persuade le président des États-Unis Franklin Roosevelt de retarder le débarquement promis, les alliés n'ayant pas encore les forces adéquates pour une opération de cette ampleur.

Après la tentative du débarquement de Dieppe du 19 août 1942 où les troupes canadiennes ont essuyé un cuisant échec sous le feu allemand du haut des falaises du pays de Caux, les Alliés ont attendu presque 2 ans pour retenter un débarquement dans le nord-ouest de l'Europe. Entretemps, ils ont développé un savoir-faire et acquis de l'expérience en réussissant plusieurs autres débarquements : en Afrique du Nord en novembre 1942, en Sicile en juillet 1943 suivi de ceux dans la botte italienne en septembre 1943 ainsi que, pour les seuls Américains, différents débarquements dans des îles du Pacifique depuis 1942 ; le débarquement de Normandie a pu ainsi tirer parti des retours d'expérience de ces précédentes opérations amphibies et choisir les plages du Calvados et du sud-est du Cotentin, plus faciles d'accès. Mais après le débarquement, les difficultés rencontrées avec le blocage devant Caen et l'obligation de progresser dans l'épais bocage normand dans ce qui sera appelé plus tard la bataille des Haies ont été une mauvaise surprise2.


