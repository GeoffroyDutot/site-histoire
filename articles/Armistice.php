<h2>Armistice 14 18</h2>
<p>Photo prise juste après la signature de l'Armistice avec au premier plan le maréchal Foch, encadré par les amiraux britanniques Hope et Rosslyn Wemyss
Le 11 novembre 1918 vers 5 h 30 du matin, juste après la signature du traité, à la sortie du « wagon de l'Armistice » : au premier plan avec une canne et un képi le maréchal Foch, entouré des amiraux britanniques Hope (à droite) et Wemyss (à gauche).
L’armistice de 1918, signé le 11 novembre 1918 à 5 h 151,2, marque la fin des combats de la Première Guerre mondiale (1914-1918), la victoire des Alliés et la défaite totale de l'Allemagne, mais il ne s'agit pas d'une capitulation au sens propre.

Le cessez-le-feu est effectif à 11 heures1,2, entraînant dans l'ensemble de la France des volées de cloches et des sonneries de clairons, et annonçant la fin d'une guerre qui a fait pour l'ensemble des belligérants plus de 18,6 millions de morts, d'invalides et de mutilés, dont 8 millions de civils. Les représentants allemands et alliés se réunissent dans un wagon-restaurant aménagé provenant du train d'état-major du maréchal Foch, dans la clairière de Rethondes, en forêt de Compiègne.

Plus tard, le 28 juin 1919, à Versailles, est signé le traité de paix, qui met réellement fin à l'état de guerre.

L'état-major souhaite un lieu isolé des regards capable d'accueillir deux trains : un pour les Alliés et l'autre pour les Allemands. L'ancien épi de tir désaffecté du Francport est redécouvert par hasard. Il convient parfaitement. Il est proche de la gare de Rethondes18, ce qui permet de ravitailler en eau les machines qui sont en permanence maintenues en chauffe, et il est assez éloigné pour permettre des discussions loin des regards. Les journalistes sont tenus volontairement à l'écart. Un chemin en caillebotis est installé entre les deux trains pour permettre les déplacements des plénipotentiaires. L'Armistice est signé dans le wagon-restaurant du train français. Ce dernier est ensuite transformé en musée. L'armistice du 22 juin 1940, cette fois-ci demandé par la France à l'Allemagne après la bataille de France, fut signé par la volonté d'Hitler dans cette même voiture historique placée exactement au même endroit qu'en 1918, selon le désir d'Hitler, montrant ainsi son esprit de revanche envers la France, qui, selon lui, avait humilié l'Allemagne à la fin de la Première Guerre mondiale. Hitler se venge ainsi du diktat de Versailles En 1940, le Führer le fait emmener à Berlin où il est évacué dans une ville voisine (Ohrdruf) lors de l'avancée des armées alliées. Il sera détruit par accident sur une voie de garage dans la gare de Crawinkel . Une reconstitution a été réalisée dans un wagon identique (le VR 2439) et est aujourd’hui présentée en forêt de Compiègne19.
