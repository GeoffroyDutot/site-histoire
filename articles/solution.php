<h2>Auschwitz et la solution final</h2>
<p>Auschwitz était constitué principalement de trois camps :

Auschwitz I, ouvert le 20 mai 1940 — le camp souche (principal) est un camp de concentration où périrent près de 70 000 personnes, au début des prisonniers de guerre et des opposants politiques polonaisa et soviétiques ; ensuite des Juifs et des résistants de toutes nationalités ;
Auschwitz II (Birkenau), ouvert le 8 octobre 1941, d’abord pour les prisonniers de guerre soviétiques — à la fois camp de concentration et centre de mise à mort immédiate où périrent plus d'un million de personnes, juives dans leur immense majorité ainsi que des Tziganes ;
Auschwitz III (Monowitz), ouvert le 31 mai 1942 — un camp de travail pour la société IG Farben.
Ces trois camps étaient complétés par une cinquantaine de petits camps dispersés dans la région et placés sous la même administration. 
Auschwitz (en allemand : Konzentrationslager Auschwitz Écouter, « camp de concentration d'Auschwitz ») est le plus grand complexe concentrationnaire du Troisième Reich, à la fois camp de concentration et d'extermination. Il est situé à une cinquantaine de kilomètres à l'ouest de Cracovie, sur le territoire des localités d'Oświęcim (Auschwitz en allemand) et de Brzezinka (Birkenau en allemand), annexées au Reich (province de Silésie) après l'invasion de la Pologne en septembre 1939.

Le camp de concentration, dirigé par les SS, est créé le 27 avril 1940 à l'initiative de Heinrich Himmler1 ; il est complété par un camp d’extermination (dont la construction démarre à la fin de 1941) et par un second camp de concentration destiné au travail forcé (créé au printemps 1942). Ces camps sont libérés par l'Armée rouge le 27 janvier 1945.

En cinq années, plus d’un million cent mille hommes, femmes et enfants meurent à Auschwitz, dont 900 000 le jour même de leur arrivée, en général par train. Sur l'ensemble des victimes, 90 % d'entre elles étaient juives, soit environ un million. Les victimes, de ce que les nazis appelèrent la « Solution finale », furent assassinées dans les chambres à gaz ou parfois par arme à feu, mais elles moururent aussi de maladies, de malnutrition, de mauvais traitements ou d'expériences médicales.

En raison de sa taille, Auschwitz est considéré comme le symbole des meurtres de masse commis par les nazis et plus particulièrement comme celui de la Shoah, au cours de laquelle près de six millions de Juifs furent assassinés.

Comme les autres camps de concentration nazis, Auschwitz était placé sous les ordres de Heinrich Himmler et de la SS. Le responsable du camp fut le SS-Obersturmbannführer Rudolf Höss (du 1er mai 1940 au 1er décembre 1943, puis de nouveau entre mai et septembre 1944), remplacé entretemps par Arthur Liebehenschel, et ensuite par Richard Baer.



