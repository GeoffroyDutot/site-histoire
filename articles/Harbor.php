<h2>Pearl Harbor</h2>
<p>L’attaque de Pearl Harbor est une attaque surprise menée par les forces aéronavales japonaises le 7 décembre 1941 contre la base navale américaine de Pearl Harbor située sur l’île d’Oahu, dans le territoire américain d’Hawaï. Autorisé par l'empereur du Japon Hirohito en réaction à l'embargo pétrolier imposé par les États-Unis cinq mois plus tôt suite à l'expansionnisme du Japon Shōwa, elle visait à détruire la flotte du Pacifique de l’United States Navy. Cette attaque entraîna l'entrée en guerre des États-Unis lors de la Seconde Guerre mondiale.

L’anéantissement de la principale flotte de l'U.S. Navy devait permettre à l’Empire du Japon de continuer à établir sa « sphère de coprospérité de la grande Asie orientale » en privant les Américains des moyens de s'y opposer militairement ; c'était aussi une réponse aux sanctions économiques prises par Washington en juillet 1941, contre sa politique impérialiste, après l'invasion de la Chine et de l'Indochine française.

L'attaque, dirigée par le général Hideki Tōjō, est lancée le dimanche 7 décembre à 7 h 48 par le Service aérien de la Marine impériale japonaise contre la flotte américaine du Pacifique et les forces stationnées sur place. Elle est conduite en deux vagues aériennes parties de six porte-avions impliquant plus de 400 avions. En moins de vingt-quatre heures, l'Empire du Japon attaqua également les États-Unis aux Philippines et engagea les hostilités avec le Royaume-Uni, en envahissant Hong Kong et en débarquant en Malaisie.

Les pertes américaines furent importantes : 2 403 morts et 1 178 blessés. Mais seulement deux cuirassés furent détruits (le troisième n'étant qu'un bateau cible) ainsi que 188 avions. Les seize autres navires endommagés furent remis en état dans les mois qui suivirent (dont onze avant la fin de 1942). Parmi les navires endommagés figuraient six cuirassés, trois croiseurs, quatre destroyers. Les trois porte-avions du Pacifique, alors absents de Pearl Harbor, étaient intacts. Les Japonais perdirent 64 hommes, 29 avions et cinq sous-marins de poche ; un marin fut capturé.

Aux États-Unis, cette attaque reste un des événements les plus marquants de l'histoire du pays et est synonyme de désastre national – chaque année le drapeau est mis en berne le 7 décembre. Les historiens ont mis en évidence l’audace du plan de l’amiral Isoroku Yamamoto, le manque de préparation et les négligences américaines. Le rôle du président Roosevelt reste un sujet de polémique.</p>



