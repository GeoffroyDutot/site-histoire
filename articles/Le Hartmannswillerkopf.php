<h2>Le Hartmannswillerkopf, le mangeur d'hommes</h2>
<p>Le Hartmannswillerkopf, rebaptisé Vieil Armand après la Première Guerre mondiale, est un éperon rocheux pyramidal, dans le massif des Vosges, surplombant de ses 956 mètres la plaine d'Alsace du Haut-Rhin.

Un monument national y est érigé en souvenir des combats qui s'y déroulèrent durant le premier conflit mondial, en 1915 surtout. En effet, situé en droite ligne à 7 km de Thann et 6 km de Cernay, entre lesquelles passait la ligne séparant la partie de l'Alsace redevenue française dès 1914 et celle reprise par les troupes allemandes, le Vieil Armand était un sommet stratégique alors âprement disputé.

Les principaux combats eurent lieu les 19 janvier-20 janvier, 26 mars, 25 avril-26 avril et 21 décembre-22 décembre 1915 faisant près de vingt-cinq mille morts dont une majorité de Français2. Parmi ces nombreux morts, on relève le général Marcel Serret et le capitaine Joseph Ferdinand Belmont. Ensuite le front s'est stabilisé et ne donna lieu qu'à des duels d'artillerie et qui a valu au sommet le nom de Montagne Sacrée d'Alsace. Au sommet, au niveau de la croix, il y a environ 22 mètres qui séparent les lignes allemandes des lignes françaises. L'inconvénient de cette situation est que les lignes doivent constamment être silencieuses, car elles peuvent s'écouter les unes les autres, et donc découvrir les stratégies de l'ennemi.

Dans l'impressionnante organisation défensive allemande, des sculptures d'Antoine Bourdelle illustrent le sacrifice de la jeunesse de ces pays voisins et cousins. Pour en sentir le pathos, il faut faire comme ces soldats du Kaiser qui gravissaient les 560 marches de la « Himmelsleiter » (échelle du ciel) qui commence à 790 m d'altitude sur le Bergpfad au versant sud.
</p>

