<h2>Les bombardements atomiques d'Hiroshima et Nagaski</h2>
<p>Le 21 juillet 1945, le président Harry S. Truman approuve le largage des bombes sur le Japon. Le 24 juillet, l'ordre est relayé par le secrétaire de la Guerre, Henri Stimson, et le lendemain, le général Thomas Handy envoie un ordre secret au général Spaatz, autorisant le largage de la bombe après le 3 août, dès que le temps le permettra, sur Hiroshima, Kokura, Niigata ou Nagasaki27. Ce sera le seul ordre écrit concernant l'utilisation de la bombe atomique. Spaatz est chargé d'en informer Mac Arthur et Nimitz. L'ordre n'évoque pas la nature de l’explosif, se contentant de mentionner une bombe spéciale.

Seules quelques personnes étaient au courant des ordres donnés par le président Truman.

Hiroshima était la cible prioritaire pour le bombardement. Le 6 août 1945, le temps était clair au-dessus de la ville. Plusieurs B-29 (dont Jabbit III pour Kokura et Full House pour Nagasaki) avaient été envoyés sur les autres cibles pour y évaluer la situation météo, au cas où les conditions seraient défavorables au-dessus d'Hiroshima, mais les autres villes étaient toutes couvertes par des nuages. Pilotée par Paul Tibbets, Enola Gay était partie à 2 h 45 de l'île de Tinian, avec Little Boy à son bord. Celle-ci fut armée pendant le vol par le capitaine de marine William Parsons.



Environ une heure avant le bombardement, les Japonais avaient détecté l'approche d'un avion américain sur le Sud de l'archipel. L'alerte fut déclenchée avec des annonces à l'intention de la population et l'interruption des programmes de la radio dans plusieurs villes. L'avion survola Hiroshima et disparut. Cet avion était en fait le B-29 de reconnaissance, Straight Flush, qui signala de bonnes conditions de visibilité pour le bombardement. Les radars japonais détectèrent ensuite un nouveau groupe d'avions à haute altitude mais leur faible nombre, seulement trois, fit que l'alerte fut levée après une dizaine de minutes. Les recommandations pour la population étaient de gagner les abris si un B-29 était visible, mais aucun raid n'était attendu mis à part de la reconnaissance.

Il s'agissait en fait des trois B-29 du raid sur Hiroshima qui évoluaient à plus de 9 500 mètres d'altitude :

Le second lieutenant, Morris R. Jeppson, fut le dernier à toucher la bombe lorsqu'il plaça les fusibles d'armement. Peu avant 8 h 15, Enola Gay arriva au-dessus de la ville. L'ordre de bombarder fut donné par Tibbets et le major Thomas Ferebee l'exécuta en visant le pont Aioi, reconnaissable par sa forme en « T », qui constituait un point de repère idéal au centre de la ville. Peu après 8 h 15, la bombe « Little Boy » sortit de la soute à une altitude de 9 450 m (31 000 pieds).

Le 6 août 1945, à 8 h 16 min 2 s, après environ 43 secondes de chute libre, activée par les capteurs d'altitude et ses radars, elle explosa à 580 mètres à la verticale de l'hôpital Shima, en plein cœur de l'agglomération, à environ 300 m au sud-est du pont initialement visé, libérant une énergie équivalente à environ 15 000 tonnes de TNT33. L'explosion tua instantanément des dizaines de milliers de personnes et détruisit tout sur environ 12 km234.

Une énorme bulle de gaz incandescent de plus de 400 mètres de diamètre35 se forma en quelques fractions de seconde, émettant un puissant rayonnement thermique. En dessous, près de l'hypocentre, la température des surfaces exposées à ce rayonnement s'est élevée un bref instant, très superficiellement, à peut-être 4 000 °C36. Des incendies se déclenchèrent, même à plusieurs kilomètres. Les personnes exposées à cet éclair furent brûlées. Celles protégées à l'intérieur ou par l’ombre des bâtiments furent ensevelies ou blessées par les projections de débris quand quelques secondes plus tard l'onde de choc arriva sur elles. Des vents de 300 à 800 km/h dévastèrent les rues et les habitations. Le long calvaire des survivants ne faisait que commencer alors que le champignon atomique, aspirant la poussière et les débris, entamait son ascension de plusieurs kilomètres.

Un énorme foyer généralisé se déclencha rapidement avec des pics de température en certains endroits[pas clair]. Si certaines zones furent épargnées lors de l'explosion, elles devaient par la suite affronter un déluge de feu causé par les mouvements intenses des masses d'air. Cette « tempête de feu » fut similaire à celles provoquées par les bombardements incendiaires sur les villes allemandes.


