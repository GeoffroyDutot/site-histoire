<h2>Cérémonie de l’Armistice du 11 novembre</h2>
      <img src="images/ceremonie.jpg" alt="Macron pendant la cérémonie du 11-11-18" class="image_actu" title="Macron pendant la cérémonie du 11 novembre 2018"/>
        <p>Dimanche 11 novembre 2018, l'Élysée a célébré en grande pompe le centenaire de l'armistice de 1918 en présence de nombreux chefs d'État invités par Emmanuel Macron, dont le président américain Donald Trump, qui a vu son convoi bloqué par plusieurs militantes Femen avant d'arriver à l'Arc de Triomphe.
      Après une cérémonie de commémoration du centenaire de l'Armistice pluvieuse sur la place de l'Étoile autour de l'Arc de Triomphe, marquée par l'arrivée tardive de Donald Trump et de Vladimir Poutine,
      les chefs d'État se sont rendus à La Villette pour un Forum sur la paix où s'exprimaient notamment Emmanuel Macron et le Premier ministre du Canada Justin Trudeau. Donald Trump, chantre de l'"America First", n'a pas souhaité assister à ce forum appelé à devenir annuel, et a rendu hommage aux soldats américains tombés pendant la guerre au cimetière de Suresnes. Une manifestation a été organisée dans l'après-midi du 11 novembre pour protester contre la venue du président américain à Paris. <a href="actu/actu_ceremonie_11_11_18.html" title="Article sur la Cérémonie de l’Armistice du 11 novembre">Lire la suite.</a></p>
