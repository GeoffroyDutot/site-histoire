    <h2>Le train d'or des nazis retrouvé en Pologne serait piégé</h2>
    <img src="images/train.jpg" alt="Le train aurait été caché dans un des nombreux tunnels creusés par les nazis./AFP" class="image_actu" title="Le train aurait été caché dans un des nombreux tunnels creusés par les nazis./AFP"/>
    <p>Deux chasseurs de trésor de nationalité allemande et polonaise auraient localisé un train chargé d'or,
      caché par les nazis en avril 1945. Il aurait été retrouvé à Walbrzych, une ville au Sud-Ouest de la Pologne
      près du château de Ksiaz. Ce dernier, situé près de la frontière tchèque, servait alors de quartier général
      aux nazis pendant la Seconde guerre mondiale. <a href="actu/actu_train.html" title="Article sur un train nazi retrouvé rempli d'or">Lire la suite.</a></p>
