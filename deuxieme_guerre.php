<h2>Le débarquement de Normandie</h2>
<img src="images/article_normandie.jpg" class="image_actu">
<p>Le débarquement de Normandie, également appelé débarquement en Normandie, ou encore débarquement allié en Normandie, nom de code opération Neptunec, est une opération militaire amphibie alliée de la Seconde Guerre mondiale lancée dans la nuit du 5 au 6 juin 1944.

C'est la phase d'assaut d'une plus vaste opération, l'opération Overlord, qui vise à créer une tête de pont alliée de grande échelle dans le nord-ouest de l'Europe et l'ouverture d'un nouveau front à l'Ouest. Ce débarquement marque le début de la bataille de Normandie. <a href="articles/Normandie.php">Lire la suite</a></p>
