<h2>Pearl Harbor</h2>
<img src="images/article_bateau.jpg" class="image_actu">
<p>L’attaque de Pearl Harbor est une attaque surprise menée par les forces aéronavales japonaises le 7 décembre 1941 contre la base navale américaine de Pearl Harbor située sur l’île d’Oahu, dans le territoire américain d’Hawaï. Autorisé par l'empereur du Japon Hirohito en réaction à l'embargo pétrolier imposé par les États-Unis cinq mois plus tôt suite à l'expansionnisme du Japon Shōwa, elle visait à détruire la flotte du Pacifique de l’United States Navy. Cette attaque entraîna l'entrée en guerre des États-Unis lors de la Seconde Guerre mondiale. <a href="articles/Harbor.php">Lire la suite</a></p>
