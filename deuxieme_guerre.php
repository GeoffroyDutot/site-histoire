<h2>Auschwitz et la solution final</h2>
<img src="images/article_auschwitz.jpg" class="image_actu">
<p>Auschwitz était constitué principalement de trois camps :

Auschwitz I, ouvert le 20 mai 1940 — le camp souche (principal) est un camp de concentration où périrent près de 70 000 personnes, au début des prisonniers de guerre et des opposants politiques polonaisa et soviétiques ; ensuite des Juifs et des résistants de toutes nationalités ;
Auschwitz II (Birkenau), ouvert le 8 octobre 1941, d’abord pour les prisonniers de guerre soviétiques — à la fois camp de concentration et centre de mise à mort immédiate où périrent plus d'un million de personnes, juives dans leur immense majorité ainsi que des Tziganes ;
Auschwitz III (Monowitz), ouvert le 31 mai 1942 — un camp de travail pour la société IG Farben.
Ces trois camps étaient complétés par une cinquantaine de petits camps dispersés dans la région et placés sous la même administration. <a href="articles/solution.php">Lire la suite</a></p>
